def number_to_chinese(n):
    return CN_NUMS.get(n, str(n))

def main():
    # Phase 1: Rename files
    for old_rel, new_rel in RENAMES:
        old_path = os.path.join(BASE, old_rel)
        new_path = os.path.join(BASE, new_rel)
        if os.path.exists(old_path):
            os.makedirs(os.path.dirname(new_path), exist_ok=True)
            shutil.move(old_path, new_path)
            print(f"MOVED: {old_rel}\n   ->: {new_rel}")
        else:
            print(f"SKIP (not found): {old_rel}")

    # Phase 2: Update the title line (first # line) in each renamed file
    for _, new_rel in RENAMES:
        new_path = os.path.join(BASE, new_rel)
        if not os.path.exists(new_path):
            continue
        with open(new_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # Extract chapter number from filename
        m = re.match(r'.*/(\d+)-', new_rel)
        if m:
            old_num = int(old_rel.split('/')[-1].split('-')[0])
            new_num = int(m.group(1))
            # Replace the title line
            # Pattern: "# XX 第X章" or "# XX-第X章"
            old_cn = number_to_chinese(old_num)
            new_cn = number_to_chinese(new_num)
            
            # Replace in title line
            lines = content.split('\n')
            for i, line in enumerate(lines):
                if line.startswith('# ') and f'第{old_cn}章' in line:
                    lines[i] = line.replace(f'第{old_cn}章', f'第{new_cn}章')
                    # Also fix the number prefix
                    lines[i] = re.sub(r'^# \d+ ', f'# {new_num} ', lines[i])
                    lines[i] = lines[i].replace(f'{old_num} 第', f'{new_num} 第')
                # Fix "下一章" references
                elif '下一章' in line or '下一卷' in line:
                    # Just note it needs manual review
                    pass
            
            with open(new_path, 'w', encoding='utf-8') as f:
                f.write('\n'.join(lines))
            print(f"UPDATED title: {new_rel}")

    print("\n=== DONE ===")
    print("Next steps:")
    print("1. Update 目录 file manually")
    print("2. Check cross-chapter references in all files")
    print("3. Create missing 第四十一章-最后的考试")

if __name__ == '__main__':
    main()