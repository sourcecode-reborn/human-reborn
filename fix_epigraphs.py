for path, (new_heading, epigraph) in fixups.items():
    full_path = os.path.join(ROOT, path)
    if not os.path.exists(full_path):
        print(f'  NOT FOUND: {path}')
        continue
    
    with open(full_path, 'r', encoding='utf-8-sig') as f:
        lines = f.readlines()
    
    # Find the first line that is a heading (# ) and the separator (---)
    heading_idx = None
    sep_idx = None
    for i, line in enumerate(lines):
        stripped = line.strip()
        if stripped.startswith('# ') and heading_idx is None:
            heading_idx = i
        if stripped == '---' and heading_idx is not None:
            sep_idx = i
            break
    
    if heading_idx is None:
        print(f'  NO HEADING: {path}')
        continue
    
    new_lines = lines[:heading_idx]  # lines before heading (usually empty)
    new_lines.append(new_heading + '\n')
    new_lines.append('\n')
    new_lines.append(epigraph)
    new_lines.append('\n')
    
    # If there was a separator, skip it; then add the rest
    if sep_idx is not None:
        new_lines.extend(lines[sep_idx+1:])
    else:
        # If no separator, skip the heading line and add rest
        new_lines.extend(lines[heading_idx+1:])
    
    with open(full_path, 'w', encoding='utf-8-sig') as f:
        f.writelines(new_lines)
    
    print(f'  FIXED: {path}')

print('\nDone!')